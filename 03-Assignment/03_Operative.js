// Operative

// You can use any non-comparison operators along with the = sign to do that thing
// Examples:
let myAge = 0
myAge += 5
myAge // 5
myAge -= 19
myAge // -14

myAge += 16 // 2
myAge %= 2 // 0
myAge -= -5 // 5
myAge *= 2 // 10
myAge **= 2 // 100
myAge -= 99 // 1

