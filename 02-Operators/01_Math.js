// Math

// You can use normal math operators in javascript

// Addition & Subtraction
2 + 2 // 4
.1 + .5 // 0.6
5 + .3 // 5.3
-5 + 2 // -3
1 - 2 // -1
1.6 - 6.2 // -4.6
.5 - 1 // -0.5
1 - -1 // 2

// Multiplication & Division
6 * .5 // 3
5 * 2 // 10
.6 * .6 // 0.36
2 / 2 // 1
5 / .2 // 25
-1 / 8 // -0.125

// Modulo
// Modulo is the remainder if 2 numbers, like in long division
5 % 2 // 1
61 % 7 // 5
99 % 4 // 3

// Exponents (Only in ES6)*
4 ** 2 // 16
3 ** 1 // 3
36 ** 0.5 // 6
3 ** 3 // 27

