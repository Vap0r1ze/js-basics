// Bitwise

// These have to do with the binary inside the numbers
// and because of that, I will use binary number literals as the operands

// Logic

// AND gate
0b0110 & 0b0101 // 4 (0b0010)
0b101 & 0b010 // 0
// OR gate
0b0100 | 0b0101 // 5 (0b0101)
0b010 | 0b110 // 6 (0b110)
// XOR gate
0b011 ^ 0b110 // 5 (0b101)
0b11 ^ 0b10 // 1 (0b01)

// Bitshift

// This will shift the bits of the number x times in a certain direction
// Shift left
0b0101 << 1 // 10 (0b1010)
0b001 << 2 // 4 (0b100)
// Shift right
0b001 >> 2 // 0 (0b000)
0b110 >> 1 // 3 (0b011)
