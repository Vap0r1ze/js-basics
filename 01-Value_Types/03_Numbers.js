// Numbers

// You don't need any kind of special format for decimal numbers (base 10)

23
512
23.12
3.141562653589
-24.2
-1
0.00001

// All valid numbers

// Scientific-notation also works

1e4 // 1000
3.141e5 // 314100
2e-1 // 0.5

// There are some special radix* form literals

// Binary (base 2)
// Begin your binary with 0b
0b101 // 5
0b1110 // 14

// Octal (base 8)
// Any number that starts with a zero is an octal literal
0777 // 511
022 // 18

// Hexidecimal (base 16)
// Prefix your hex with 0x
0xFF // 255
0xFF0000 // 16711680
// Case insensitive
0x7289da // 7506394

// * A radix is the amount of digits in a number system
//   eg: The radix for binary is 2, because there is 0 and 1
